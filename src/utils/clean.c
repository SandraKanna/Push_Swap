#include "push_swap.h"

void	free_stack(t_node **stack)
{
	t_node	*temp;

	if (!*stack)
		return ;
	while (*stack)
	{
		temp = (*stack)->next;
		free(*stack);
		*stack = temp;
	}
}

void	free_struct(t_struct *structure)
{

	if (structure->a != NULL)
		free_stack(&structure->a);
	if (structure->b != NULL)
		free_stack(&structure->b);
	free (structure);
}

void	err_handling(t_struct *structure)
{
	free_struct(structure);
	exit (EXIT_FAILURE);
}
